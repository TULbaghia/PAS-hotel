package pl.lodz.p.pas.model.apartment.exception;

public class ThreeStarApartmentException extends ApartmentException {

    public ThreeStarApartmentException() {
        super();
    }

    public ThreeStarApartmentException(String message) {
        super(message);
    }

    public ThreeStarApartmentException(Throwable cause) {
        super(cause);
    }

    public ThreeStarApartmentException(String message, Throwable cause) {
        super(message, cause);
    }

}